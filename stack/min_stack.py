class MinStack:

    def __init__(self):
        self.stack = []   
        self.minStack = []

    def push(self, val: int) -> None:
        self.stack.append(val)
        if len(self.stack) == 1:
            self.minStack.append(val)
        else:
            self.minStack.append(min(val, self.minStack[-1]))

    def pop(self) -> None:
        self.stack.pop()
        self.minStack.pop()

    def top(self) -> int:
       return self.stack[-1] 

    def getMin(self) -> int:
        return self.minStack[-1]

#Time Complexity: each function runs at O(1) constatn time